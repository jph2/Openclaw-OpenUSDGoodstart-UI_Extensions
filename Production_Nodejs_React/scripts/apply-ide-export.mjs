#!/usr/bin/env node
/**
 * Apply Channel Manager IDE export (B): materialize `.cursor/agents/*.md` from
 * GET /api/exports/ide or from local channel_config.json + ideConfigBridge.
 *
 * Writes main engines (tars/marv/case…) and sub-agents. Updates
 * `.cursor/cm-ide-export-fingerprint.json` on --write for stale checks.
 *
 * Does not touch ~/.cursor — only --target (repo root).
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

import { buildCanonicalSnapshot, buildIdeWorkbenchBundle } from '../backend/services/ideConfigBridge.js';
import { computeIdeExportFingerprint, FINGERPRINT_SCHEMA } from './ideExportFingerprint.mjs';

const GENERATED_FIRST_LINE = '<!-- cm-ide-export:v1 managed-by-channel-manager-apply-script -->';

function parseArgs(argv) {
    const out = {
        dryRun: true,
        write: false,
        force: false,
        target: '',
        apiBase: '',
        config: ''
    };
    for (let i = 2; i < argv.length; i++) {
        const a = argv[i];
        if (a === '--dry-run') out.dryRun = true;
        else if (a === '--write') {
            out.write = true;
            out.dryRun = false;
        } else if (a === '--force') out.force = true;
        else if (a === '--target' && argv[i + 1]) out.target = argv[++i];
        else if (a === '--api-base' && argv[i + 1]) out.apiBase = argv[++i];
        else if (a === '--config' && argv[i + 1]) out.config = argv[++i];
        else if (a === '--help' || a === '-h') out.help = true;
    }
    return out;
}

function defaultConfigPath() {
    const root = process.env.WORKSPACE_ROOT;
    if (!root) return '';
    return path.join(
        root,
        'OpenClaw_Control_Center',
        'Prototyp',
        'channel_CHAT-manager',
        'channel_config.json'
    );
}

async function loadBundle({ apiBase, configPath }) {
    if (apiBase) {
        const base = apiBase.replace(/\/$/, '');
        const url = `${base}/api/exports/ide`;
        const res = await fetch(url, { headers: { Accept: 'application/json' } });
        if (!res.ok) {
            throw new Error(`GET ${url} failed: HTTP ${res.status} ${await res.text()}`);
        }
        const body = await res.json();
        if (!body.ok || !body.data) {
            throw new Error(`Unexpected API response: ${JSON.stringify(body).slice(0, 500)}`);
        }
        return body.data;
    }
    const cfgResolved = configPath || defaultConfigPath();
    if (!cfgResolved) {
        throw new Error('Set WORKSPACE_ROOT or pass --config /path/to/channel_config.json or --api-base URL');
    }
    const raw = JSON.parse(await fs.readFile(cfgResolved, 'utf8'));
    const snap = buildCanonicalSnapshot(raw);
    return buildIdeWorkbenchBundle(snap);
}

function buildSubagentMarkdown(entry) {
    const fm = entry.suggestedFrontmatter || {};
    const desc = String(fm.description || `${entry.displayName} — parent: ${entry.parentEngine}`);
    const skills = Array.isArray(entry.skillIds) ? entry.skillIds : [];
    const skillsLine = skills.length ? skills.map((s) => `- \`${s}\``).join('\n') : '_— none in CM additionalSkills —_';

    return `${GENERATED_FIRST_LINE}
---
name: ${fm.name || entry.name}
description: |
  ${desc.replace(/\n/g, '\n  ')}
model: ${fm.model ?? 'inherit'}
---

# ${entry.displayName || entry.name}

**CM sub-agent id:** \`${entry.name}\`  
**Parent engine:** \`${entry.parentEngine}\`

## Skills (IDs from Channel Manager)

${skillsLine}

_Generated by \`scripts/apply-ide-export.mjs\`. Source of truth: \`channel_config.json\` + CM Apply → OpenClaw. Refresh after CM edits._

---

_Add workflow-specific instructions below this line (preserved only if you maintain them outside the generated block — this file is fully replaced when the script runs with \`--force\` or when the CM marker is present)._
`;
}

function buildEngineMarkdown(engine) {
    const id = String(engine.id || 'engine');
    const name = String(engine.name || id);
    const desc = `${name} — **main agent (engine)** from Channel Manager`;
    const skills = [...(engine.defaultSkills || [])];
    const skillsLine = skills.length ? skills.map((s) => `- \`${s}\``).join('\n') : '_— none in CM defaultSkills —_';

    return `${GENERATED_FIRST_LINE}
---
name: ${id}
description: |
  ${desc.replace(/\n/g, '\n  ')}
model: inherit
---

# ${name}

**CM main agent id:** \`${id}\`

## Default skills (IDs from Channel Manager)

${skillsLine}

_Sub-agents with \`parent: ${id}\` are separate files in this folder._

_Generated by \`scripts/apply-ide-export.mjs\`. Refresh after CM edits._

---

_Add triad / persona prose below this line if needed._
`;
}

async function pathExists(p) {
    try {
        await fs.access(p);
        return true;
    } catch {
        return false;
    }
}

function buildPlan(targetRoot, bundle) {
    const byRel = new Map();
    for (const engine of bundle.engines || []) {
        const id = String(engine.id || '').trim();
        if (!id) continue;
        const rel = `.cursor/agents/${id}.md`;
        const abs = path.join(targetRoot, rel.split('/').join(path.sep));
        byRel.set(rel, { abs, rel, content: buildEngineMarkdown(engine), name: id, kind: 'engine' });
    }
    for (const entry of bundle.subagents || []) {
        const rel = entry.relativePath || `.cursor/agents/${entry.name}.md`;
        const abs = path.join(targetRoot, rel.split('/').join(path.sep));
        byRel.set(rel, { abs, rel, content: buildSubagentMarkdown(entry), name: entry.name, kind: 'subagent' });
    }
    return [...byRel.values()].sort((a, b) => a.rel.localeCompare(b.rel));
}

async function main() {
    const args = parseArgs(process.argv);
    if (args.help) {
        console.log(`apply-ide-export.mjs — materialize CM IDE bundle under .cursor/agents/

  --target <dir>     Repo root (required): writes <dir>/.cursor/agents/
  --dry-run          Print planned writes (default if --write not passed)
  --write            Write files + update cm-ide-export-fingerprint.json
  --force            Overwrite files that lack the CM marker
  --api-base <url>   e.g. http://127.0.0.1:3000 — fetch /api/exports/ide
  --config <path>    channel_config.json (else WORKSPACE_ROOT default)

Writes: sub-agents + main engines (tars, marvin, case, …). Stale check:
  npm run check-ide-export-stale -- --target <same-dir>

Env: WORKSPACE_ROOT — used for default --config path when --api-base is omitted
`);
        process.exit(0);
    }

    if (!args.target) {
        console.error('Error: --target <repo-root> is required.');
        process.exit(1);
    }

    const targetRoot = path.resolve(args.target);
    const bundle = await loadBundle({ apiBase: args.apiBase, configPath: args.config });

    if (bundle.kind !== 'ide_workbench_bundle' && bundle.kind !== 'cursor_bundle') {
        console.warn('Warning: bundle.kind is not ide_workbench_bundle — proceeding anyway.');
    }

    const plan = buildPlan(targetRoot, bundle);
    const agentsDir = path.join(targetRoot, '.cursor', 'agents');
    const fingerprint = computeIdeExportFingerprint(bundle);

    if (args.dryRun || !args.write) {
        const subs = plan.filter((p) => p.kind === 'subagent').length;
        const eng = plan.filter((p) => p.kind === 'engine').length;
        console.log(`Dry run: ${plan.length} file(s) (${subs} sub-agents, ${eng} engines) → ${agentsDir}`);
        console.log(`Fingerprint would be: ${fingerprint}`);
        for (const p of plan) {
            const exists = await pathExists(p.abs);
            let note = exists ? '(exists)' : '(new)';
            if (exists) {
                const prev = await fs.readFile(p.abs, 'utf8');
                const safe = prev.startsWith(GENERATED_FIRST_LINE) || args.force;
                note += safe ? '' : ' SKIP (no CM marker; use --force)';
            }
            console.log(`  [${p.kind}] ${p.rel} ${note}`);
        }
        console.log('\nPass --write to apply.');
        process.exit(0);
    }

    await fs.mkdir(agentsDir, { recursive: true });
    let written = 0;
    let skipped = 0;

    for (const p of plan) {
        const exists = await pathExists(p.abs);
        if (exists) {
            const prev = await fs.readFile(p.abs, 'utf8');
            if (!prev.startsWith(GENERATED_FIRST_LINE) && !args.force) {
                console.warn(`Skip (no CM marker): ${p.rel}`);
                skipped++;
                continue;
            }
        }
        await fs.mkdir(path.dirname(p.abs), { recursive: true });
        await fs.writeFile(p.abs, p.content, 'utf8');
        console.log(`Wrote ${p.rel}`);
        written++;
    }

    const fpPath = path.join(targetRoot, '.cursor', 'cm-ide-export-fingerprint.json');
    await fs.mkdir(path.dirname(fpPath), { recursive: true });
    await fs.writeFile(
        fpPath,
        `${JSON.stringify(
            {
                schema: FINGERPRINT_SCHEMA,
                fingerprint,
                generatedAt: new Date().toISOString(),
                counts: {
                    files: plan.length,
                    written,
                    skipped
                }
            },
            null,
            2
        )}\n`,
        'utf8'
    );
    console.log(`Wrote .cursor/cm-ide-export-fingerprint.json`);

    console.log(`\nDone. Written: ${written}, skipped: ${skipped}`);
    if (skipped > 0) {
        console.log('Use --force to replace files without the CM marker.');
    }
}

const __filename = fileURLToPath(import.meta.url);
if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
    main().catch((e) => {
        console.error(e.message || e);
        process.exit(1);
    });
}
